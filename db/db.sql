  CREATE OR REPLACE PACKAGE BODY "TEST4"."P_HR_ATTENDANCE" AS

PROCEDURE P_CREATE_DAILY_ATTD(
         p_base_day IN VARCHAR2,
          p_error_code OUT VARCHAR2,
          p_error_msg OUT VARCHAR2
)
IS
   v_close_yn VARCHAR2(256);                      --마감여부
   v_yoil VARCHAR2(256);                             --요일
    v_holiday_yn VARCHAR2(256);                       --휴일여부
    v_holiday_date VARCHAR2(256);                    --휴일날짜
    v_holiday_type VARCHAR2(256);                    --휴일타입

    v_base_attend_time VARCHAR2(256);                 --기본출근시각
    v_base_quit_time VARCHAR2(256);                --기본퇴근시각
    v_base_over_time VARCHAR2(256);                --기본연장시각
    v_base_night_time VARCHAR2(256);                  --기본심야시각
    v_base_lunch_start_time VARCHAR2(256);               --기본점심시작시각
    v_base_lunch_end_time VARCHAR2(256);               --기본점심종료시각
    v_apply_year VARCHAR2(256);                          --기준년도
    v_temp VARCHAR2(256);
    v_sdf VARCHAR2(256);
    
     
     v_common_quit_time VARCHAR2(256); --일반퇴근시간
     v_after_mn_quit_time VARCHAR2(256); --심야 자정넘어서 퇴근시간
     
     

BEGIN
    v_holiday_yn:='Y';
    v_temp:='';
    v_apply_year :='2018';      --현재 기준 년도
   SAVEPOINT SP1;


   -- *************************************************************************
    --  회사 기초 근무시간 가져오기.(출근퇴근타입코드값 입력할때 이용)
    -- *************************************************************************

   BEGIN
     SELECT
        ATTEND_TIME,
        QUIT_TIME,
        OVER_TIME,
        NIGHT_TIME,
        LUNCH_START_TIME,
        LUNCH_END_TIME
     INTO
        v_base_attend_time,            --기본출근시각
       v_base_quit_time,              --기본퇴근시각
        v_base_over_time,              --기본연장시각
         v_base_night_time,              --기본심야시각
         v_base_lunch_start_time,         --기본점심시작시간
          v_base_lunch_end_time       --기본점심종료시간
       FROM
           BASE_WORK_TIME
      WHERE
           APPLY_YEAR=v_apply_year;

      EXCEPTION
           WHEN NO_DATA_FOUND THEN
             p_error_code:='-1';
            p_error_msg:=' 해당년도에 등록된 회사 기본근무시간이 없습니다.';
            ROLLBACK TO SP1;
            RETURN;
  END;

   -- **************************************************************************
    -- 마감된 일자는 일근태관리(DAY_ATTD_MGT)에서 삭제안하고 프로시저 종료.
    -- 마감되지 않은 일자는 일근태관리(DAY_ATTD_MGT)에서 삭제후 새로운 데이터 삽입.
    -- 마감구분 ( 마감 = 'Y' , 마감안함 = 'N')
    -- 새로운 마감목록이 들어왓을경우 마감취소후 다시마감
    -- ************************************************************************

   BEGIN
       SELECT
           DISTINCT(CLOSE_YN)
         INTO v_close_yn         -- 마감여부 
      FROM
         DAY_ATTD_MGT            -- 일근태관리
      WHERE
         BASIC_DAY = p_base_day;  

      EXCEPTION
         WHEN OTHERS THEN
           p_error_code:='0';
            p_error_msg:='Continue Procedure';
  END;

      -- 기준일의 데이터가 마감되어있으면 프로시저 종료
  IF v_close_yn = 'Y' THEN
     p_error_code:='0';
    p_error_msg:='P_CREATE_DAILY_ATTD success';
     RETURN;

   -- ************************************************************************
    -- 일근태현황(DAY_ATTD_MGT) 테이블에 기준일에 대한 데이터를 넣는다
    -- 마감시키지않고 다시 프로시저 호출하엿을때 기준일의 일근태현황 데이터를 모두 지운다
    -- ************************************************************************

  ELSE

  BEGIN
       DELETE FROM
            DAY_ATTD_MGT
      WHERE
            BASIC_DAY=p_base_day;

      -- 예외발생시 프로시저 종료
      EXCEPTION
        WHEN OTHERS THEN
           p_error_code:='-1';
          p_error_msg:='기준일의 일근태현황(DAY_ATTD_MGT) 데이터 삭제실패';
          ROLLBACK TO SP1;
          RETURN;
  END;

  --모든사원을 forin으로 돌려서 모든사원의 일근태계산한다

   FOR FOR_EMPLOYEE IN(
       SELECT * FROM EMPLOYEE
  )LOOP
     BEGIN
           INSERT INTO
                      DAY_ATTD_MGT(
                    EMP_CODE,
                    BASIC_DAY,
                    CLOSE_YN
            )VALUES(
                        FOR_EMPLOYEE.EMP_CODE,
                      p_base_day,
                      'N'
            );

        EXCEPTION
           WHEN DUP_VAL_ON_INDEX THEN     -- 무결성 제약조건 위반
                p_error_code:='-1';
              p_error_msg:='데이터가 이미 존재합니다.';
              ROLLBACK TO SP1;
              RETURN;
          WHEN OTHERS THEN
                p_error_code:='-1';
              p_error_msg:='기준일 데이터 생성 실패.';
              ROLLBACK TO SP1;
              RETURN;
     END;
  END LOOP;
  
      -- ************************************************************************
    -- 기준일에 해당하는 요일을 구하여 일근태관리(DAY_ATTD_MGT) 데이터를 업데이트한다.
    -- ************************************************************************

  BEGIN
       SELECT
           TO_CHAR(TO_DATE(p_base_day,'YYYYMMDD'),'DAY','NLS_DATE_LANGUAGE=Korean') -- 무슨 요일인지 구해서 
      INTO v_yoil -- 변수에 담아준다.
      FROM dual;

      UPDATE
           DAY_ATTD_MGT
      SET
           DAILY=v_yoil
      WHERE
           BASIC_DAY=p_base_day; -- 변수에 담아준 요일을 DAY_ATTD_MGT 테이블에 기준일에 해당하는 칼럼에 넣어준다.
  END;


      -- ************************************************************************
    -- 근무일구분(현재날짜가 휴일인지 평일인지 구분한다)
    -- AQC009 휴일
    -- AQC010 정상근무일
    -- ************************************************************************
    
    
     
     BEGIN
      SELECT
           BASIC_DAY
      INTO v_holiday_date
      FROM
           HOLIDAY_MGT
      WHERE BASIC_DAY=p_base_day;

      EXCEPTION
         WHEN NO_DATA_FOUND THEN
           v_holiday_yn := 'N';
   END;

   
  IF  v_holiday_yn='Y' OR v_yoil='토요일' OR v_yoil='일요일' THEN-- 기준일이 휴일이라면
     UPDATE
           DAY_ATTD_MGT
      SET
           attd_day_type='AQC009' 
      WHERE
           BASIC_DAY=p_base_day;
            
  ELSE  -- 기준일이 근무일이라면

      UPDATE
           DAY_ATTD_MGT
      SET
           attd_day_type='AQC010'
      WHERE
           BASIC_DAY=p_base_day;

   END IF;


    -- ************************************************************************
    -- 출근시간이 없는 사원 처리 ( 휴일 고려 ) 먼저 휴무인지 결근인지 싹업데이트시킨후 후에 출근시간이
    -- 잇는사원은 업데이트를 하여 처리시킨다
   -- NO_DATA_FOUND 시 평일
    -- ************************************************************************

  IF v_holiday_yn='N' THEN
       BEGIN
           UPDATE
                DAY_ATTD_MGT
          SET
                ATTEND_TYPE_CODE = 'AQC007',
               QUIT_TYPE_CODE = 'AQC007'
          WHERE
               ATTEND_TIME IS NULL
          AND
               BASIC_DAY=p_base_day;

          EXCEPTION
               WHEN OTHERS THEN
                     p_error_code:='-1';
                   p_error_msg:='평일 결근처리 실패.';
                   ROLLBACK TO SP1;
                      RETURN;
      END;

  ELSE  --휴일
     BEGIN
          SELECT HOLIDAY_TYPE
            INTO v_holiday_type
           FROM HOLIDAY_MGT
          WHERE BASIC_DAY=p_base_day;

    IF v_holiday_type = 'HGC001' THEN
          UPDATE
              DAY_ATTD_MGT
        SET
              ATTEND_TYPE_CODE = 'AQC009',  --휴일
            QUIT_TYPE_CODE = 'AQC009'
        WHERE
              ATTEND_TIME IS NULL
        AND
              BASIC_DAY=p_base_day;

    -- ************************************************************************
      --휴일타입이 유급휴일이면 출/퇴근 타입을 유급휴일로 SETTING
    -- ************************************************************************
    ELSE
         UPDATE
              DAY_ATTD_MGT
        SET
              ATTEND_TYPE_CODE = 'AQC008',
            QUIT_TYPE_CODE = 'AQC008'
        WHERE
              ATTEND_TIME IS NULL
        AND
              BASIC_DAY=p_base_day;
    END IF;

      EXCEPTION
            WHEN OTHERS THEN
                  p_error_code:='-1';
              p_error_msg:='유/무급휴일처리 실패.';
              ROLLBACK TO SP1;
              RETURN;
    END;
  END IF;





    -- ************************************************************************
    -- 승인된 일근태(DAILY_ATTD) 데이터로 일근태관리(DAILY_ATTD_MGT) 데이터를 업데이트한다
    -- 출근 ADC001
    -- 퇴근 ADC002
    -- 외출 ADC003
    -- 귀사 ADC004
    -- 연장근무 AQC001
    -- 심야근무 AQC002
    -- 지각    AQC003
    -- 정상출근 AQC004
    -- 정상퇴근 AQC005
    -- 조퇴    AQC006
    -- 결근    AQC007
    -- 휴무    AQC008
    -- ************************************************************************

   FOR FOR_DAY_ATTD IN(
       SELECT
            BASIC_DAY,
          E.EMP_CODE EMP_CODE,
          ATTD_TYPE_CODE,
          TIME,
          E.EMP_NAME EMP_NAME
      FROM
           DAY_ATTD DA, EMPLOYEE E
      WHERE
           BASIC_DAY=p_base_day
      AND
           APPROVAL_STATUS = 'Y'
      AND DA.EMP_CODE=E.EMP_CODE
  )LOOP

        --출근시간 업데이트(타입이 출근이고 기준시간보다작거나 같을때) 정상출근처리시킨다

  IF FOR_DAY_ATTD.ATTD_TYPE_CODE='ADC001' AND FOR_DAY_ATTD.TIME <= v_base_attend_time THEN
           BEGIN
               UPDATE
                   DAY_ATTD_MGT
              SET
                   ATTEND_TIME=FOR_DAY_ATTD.TIME,
                  ATTEND_TYPE_CODE='AQC004'
                  --QUIT_TYPE_CODE=NULL
              WHERE
                   EMP_CODE=FOR_DAY_ATTD.EMP_CODE
              AND
                    BASIC_DAY=FOR_DAY_ATTD.BASIC_DAY;

              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                      p_error_msg:='정상출근데이터 업데이트 실패.';
                      ROLLBACK TO SP1;
                       RETURN;
          END;
      END IF;

           --지각시간 업데이트 (타입이 출근이고 시간이 기준출근시간보다 초과하엿을경우) 지각 처리 시킨다

       IF FOR_DAY_ATTD.ATTD_TYPE_CODE='ADC001' AND FOR_DAY_ATTD.TIME > v_base_attend_time THEN
           BEGIN
               UPDATE
                   DAY_ATTD_MGT
              SET
                   ATTEND_TIME=FOR_DAY_ATTD.TIME,
                  ATTEND_TYPE_CODE='AQC003'
                  --QUIT_TYPE_CODE=''
              WHERE
                   EMP_CODE=FOR_DAY_ATTD.EMP_CODE
              AND
                    BASIC_DAY=FOR_DAY_ATTD.BASIC_DAY;

              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                      p_error_msg:='출근데이터 -지각 업데이트 실패.';
                      ROLLBACK TO SP1;
                       RETURN;
          END;
      END IF;
      
      
        --퇴근시간 업데이트 (타입이 퇴근이고 시간이 기준퇴근시간과 같을경우) 정상퇴근처리한다 (연장근무신청 안했을 경우 퇴근 어떤시간에 찍던 기본근무시간 찍히도록)

      IF FOR_DAY_ATTD.ATTD_TYPE_CODE='ADC002' AND FOR_DAY_ATTD.TIME >= v_base_quit_time AND FOR_DAY_ATTD.TIME<0 THEN
           BEGIN
               UPDATE
                   DAY_ATTD_MGT
              SET
                   QUIT_TIME=v_base_quit_time,
                  QUIT_TYPE_CODE='AQC005'
              WHERE
                   EMP_CODE=FOR_DAY_ATTD.EMP_CODE
              AND
                 BASIC_DAY=FOR_DAY_ATTD.BASIC_DAY;

              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                      p_error_msg:='퇴근 데이터 업데이트 실패.';
                      ROLLBACK TO SP1;
                       RETURN;
          END;
      ELSE  --자정 후 심야 퇴근 찍었을때
       BEGIN
               UPDATE
                   DAY_ATTD_MGT
              SET
                   QUIT_TIME=v_base_quit_time,
                  QUIT_TYPE_CODE='AQC005'
              WHERE
                   EMP_CODE=FOR_DAY_ATTD.EMP_CODE
              AND
                 BASIC_DAY=FOR_DAY_ATTD.BASIC_DAY;

              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                      p_error_msg:='퇴근 데이터 업데이트 실패.';
                      ROLLBACK TO SP1;
                       RETURN;
          END;

      END IF;
           

       IF FOR_DAY_ATTD.ATTD_TYPE_CODE = 'ADC002' AND FOR_DAY_ATTD.TIME < v_base_quit_time AND FOR_DAY_ATTD.TIME > v_base_attend_time THEN
            BEGIN
                UPDATE
                   DAY_ATTD_MGT
                SET

                   EARLY_LEAVE_TIME=FOR_DAY_ATTD.TIME,
                  QUIT_TYPE_CODE='AQC006'
                WHERE
                   EMP_CODE=FOR_DAY_ATTD.EMP_CODE
                AND
                    BASIC_DAY=FOR_DAY_ATTD.BASIC_DAY;
                EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                      p_error_msg:='조퇴 데이터 업데이트 실패.';
                      ROLLBACK TO SP1;
                       RETURN;
            END;
        END IF;


        IF FOR_DAY_ATTD.ATTD_TYPE_CODE = 'ADC003' THEN
            BEGIN
                UPDATE
                   DAY_ATTD_MGT
                SET
                   LEAVE_TIME=FOR_DAY_ATTD.TIME
                WHERE
                   EMP_CODE=FOR_DAY_ATTD.EMP_CODE
                AND
                    BASIC_DAY=FOR_DAY_ATTD.BASIC_DAY;

                EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                      p_error_msg:='외출 데이터 업데이트 실패.';
                      ROLLBACK TO SP1;
                       RETURN;
            END;
        END IF;


        IF FOR_DAY_ATTD.ATTD_TYPE_CODE = 'ADC004' THEN
            BEGIN
                UPDATE
                   DAY_ATTD_MGT
                SET
                   RETURN_TIME=FOR_DAY_ATTD.TIME
                WHERE
                   EMP_CODE=FOR_DAY_ATTD.EMP_CODE
                AND
                    BASIC_DAY=FOR_DAY_ATTD.BASIC_DAY;

                EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                      p_error_msg:='귀사 데이터 업데이트 실패.';
                      ROLLBACK TO SP1;
                       RETURN;
            END;
            
            
        END IF;

  END LOOP;
  
                BEGIN
                 SELECT TIME
                 INTO v_common_quit_time    --일반퇴근시간
                 FROM
                   DAY_ATTD
                 WHERE
                     BASIC_DAY=p_base_day
                 AND ATTD_TYPE_CODE='ADC002'
                 AND TIME>0900;
                 EXCEPTION
               WHEN NO_DATA_FOUND THEN
                   v_common_quit_time := 'N';  --일반퇴근이 안찍혔을때 N 이라고 값을 준다
                END;
               
               BEGIN
                 SELECT TIME
                  INTO v_after_mn_quit_time --자정이후퇴근시간
                  FROM
                   DAY_ATTD
                  WHERE
                      BASIC_DAY=p_base_day+1 --다음날 기준으로
                  AND ATTD_TYPE_CODE='ADC002'
                  AND TIME<0900;  --퇴근시간이 9시 안으로 일경우
                   EXCEPTION
               WHEN NO_DATA_FOUND THEN
                   v_after_mn_quit_time := 'N'; --자정 넘어 심야퇴근시간이 없으면 N 이라고 설정
                END; 
  
  
--****************************************************************
--   신청해서 승인된 연장,심야 근무일 경우 코드 넣기 
--****************************************************************

    FOR for_over_night_mgt IN (    --연장심야신청에서 승인된것만 돌림
       SELECT
           *
        FROM
           OVER_NIGHT_TIME_MGT
        WHERE
           BASIC_DAY=p_base_day 
        AND
            APPROVAL_STATUS='Y'
    )LOOP
       --연장,심야근무 업데이트
       IF for_over_night_mgt.type = '연장근무' THEN
          BEGIN
              UPDATE
                  DAY_ATTD_MGT
              SET
                  QUIT_TIME=for_over_night_mgt.TO_TIME, --연장심야신청에 있는 신청시각을 퇴근시각으로 업데이트
                  ATTEND_TYPE_CODE = 'AQC001', --연장근무시작
                  QUIT_TYPE_CODE = 'AQC001'  --데이터에만 들어가고 그리드에는 안뿌려져서 상관없긴함.
              WHERE
                   BASIC_DAY = for_over_night_mgt.BASIC_DAY
              AND
                  EMP_CODE = for_over_night_mgt.EMP_CODE;
              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                        p_error_msg:='연장근무코드 업데이트 실패.';
                        ROLLBACK TO SP1;
                       RETURN;
          END;
          
          ELSE --심야근무
          
           IF  v_common_quit_time='N' AND v_after_mn_quit_time='N' THEN --퇴근이 안찍혔을 경우, 퇴근시각 0으로 셋팅
           v_common_quit_time:='0'; --일근태에서 가져올때 값넣었을때 사용했던 거 연장심야신청에서 퇴근시각가져오기때문에 지금은 필요없음
           v_after_mn_quit_time:='0';  
           ELSE
     
            IF v_common_quit_time='N' AND v_after_mn_quit_time!='N' THEN --심야 자정 넘어서  즉 당일 퇴근이 없으면서 다음날 0~9시사이에 퇴근이 찍힌 경우
           
           BEGIN
                UPDATE
                   DAY_ATTD_MGT
                SET
                  QUIT_TIME=for_over_night_mgt.TO_TIME,
                  QUIT_TYPE_CODE='AQC002',
                  ATTEND_TYPE_CODE = 'AQC002'
                WHERE
                     EMP_CODE=for_over_night_mgt.EMP_CODE
                AND
                    BASIC_DAY=for_over_night_mgt.BASIC_DAY;
                EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                        p_error_msg:='심야근무코드 업데이트 실패.';
                        ROLLBACK TO SP1;
                       RETURN;
                END;
                
                ELSE--자정전 퇴근
                 BEGIN
              UPDATE
                  DAY_ATTD_MGT
              SET
                  QUIT_TIME=v_common_quit_time,
                  ATTEND_TYPE_CODE = 'AQC002',
                  QUIT_TYPE_CODE = 'AQC002'
              WHERE
                   BASIC_DAY = for_over_night_mgt.BASIC_DAY
              AND
                  EMP_CODE = for_over_night_mgt.EMP_CODE;
              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                        p_error_msg:='심야근무코드 업데이트 실패.';
                        ROLLBACK TO SP1;
                       RETURN;
              END;
             END IF; --심야근무 상세구분 IF종료
           END IF; --일근태값 있을때 없을때 , 일근태값 퇴근시각으로 안넣을거면 이것도 필요가 없다.
          
          
          
        END IF;--심야근무 IF종료

    END LOOP;



    -- ************************************************************************
    -- 연차 테이블의(DAY_ANNUL) 데이터로 일근태관리(DAY_ATTD_MGT) 데이터를 업데이트한다
    -- 연차 AQC013
    -- ************************************************************************
    --연차가 있는 경우 연차기간을 between절로 구하여 연차를 업데이트시킨다

   FOR for_day_annul IN (
       SELECT
           *
        FROM
           DAY_ANNUAL
        WHERE
           p_base_day BETWEEN START_DATE AND END_DATE
        AND
            APPROVAL_STATUS='Y'
    )LOOP
       --연차 업데이트(연차)
       IF for_day_annul.ANNUAL_CODE = 'AQC013' THEN
          BEGIN
              UPDATE
                  DAY_ATTD_MGT
              SET
                  ATTEND_TYPE_CODE = 'AQC013',
                  QUIT_TYPE_CODE = 'AQC013'
              WHERE
                   BASIC_DAY = p_base_day
              AND
                  EMP_CODE = for_day_annul.EMP_CODE;
              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                        p_error_msg:='연차 데이터 업데이트 실패.';
                        ROLLBACK TO SP1;
                       RETURN;
          END;
        END IF;

    END LOOP;

   -- ************************************************************************
    -- 승인된 근태외(ATTD_REST_MGT) 데이터로 일근태관리(DAY_ATTD_MGT) 데이터를 업데이트한다
    -- 출장 AQC011
    -- 교육 AQC012
    -- ************************************************************************
    -- 근태외 신청이 잇는경우 between절로 기간을 구한뒤 그기간에대한 일근태를 계산한다

   FOR FOR_ATTD_REST IN (
       SELECT *
      FROM
           ATTD_REST_MGT
      WHERE
           p_base_day BETWEEN START_DATE AND END_DATE
      AND
           APPROVAL_STATUS = 'Y'
  )LOOP

       IF FOR_ATTD_REST.ATTD_REST_CODE = 'ASC002' THEN
          BEGIN
              UPDATE
                  DAY_ATTD_MGT
              SET
                  ATTEND_TYPE_CODE = 'AQC011',
                  QUIT_TYPE_CODE = 'AQC011',
                  ATTEND_TIME= v_base_attend_time,
                  QUIT_TIME= v_base_quit_time
              WHERE
                   BASIC_DAY = p_base_day
              AND
                  EMP_CODE = FOR_ATTD_REST.EMP_CODE;

              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                        p_error_msg:='근태외타입(출장) 데이터 업데이트 실패.';
                        ROLLBACK TO SP1;
                       RETURN;
          END;
      END IF;


      IF FOR_ATTD_REST.ATTD_REST_CODE = 'ASC003' THEN
          BEGIN
              UPDATE
                  DAY_ATTD_MGT
              SET
                 ATTEND_TYPE_CODE = 'AQC012',
                 QUIT_TYPE_CODE = 'AQC012',
                 ATTEND_TIME= v_base_attend_time,
                 QUIT_TIME=v_base_quit_time
              WHERE
                   BASIC_DAY = p_base_day
              AND
                  EMP_CODE = FOR_ATTD_REST.EMP_CODE;

              EXCEPTION
                   WHEN OTHERS THEN
                       p_error_code:='-1';
                      p_error_msg:='근태외타입(교육) 데이터 업데이트 실패.';
                      ROLLBACK TO SP1;
                       RETURN;
          END;
      END IF;

  END LOOP;
   COMMIT;

  BEGIN
      P_DAILY_ATTD_CALC(p_base_day,p_error_code,p_error_msg);
  END;

END IF;
    p_error_code:='0';
   p_error_msg:='P_CREATE_DAILY_ATTD Success ';
END P_CREATE_DAILY_ATTD;

--일근태를 계산하는 프로시저



PROCEDURE P_DAILY_ATTD_CALC(
      p_base_day IN VARCHAR2,            --기준일
      p_error_code OUT VARCHAR2,      --에러코드
      p_error_msg OUT VARCHAR2         --에러메세지
)
IS
      v_base_attend_time VARCHAR2(256);          --기본출근시각
      v_base_quit_time VARCHAR2(256);             --기본퇴근시각
      v_base_over_time VARCHAR2(256);             --기본연장시각
      v_base_night_time VARCHAR2(256);             --기본심야시각
      v_base_lunch_start_time VARCHAR2(256);       --기본점심시작시각
      v_base_lunch_end_time VARCHAR2(256);       --기본점심종료시각
      v_base_lunch_hour VARCHAR2(256);             --점심시간

      v_base_dinner_start_time VARCHAR2(256);       --기본저녁시작시각
      v_base_dinner_end_time VARCHAR2(256);       --기본저녁종료시각
      v_base_dinner_hour VARCHAR2(256);             --저녁시간

      v_attend_time VARCHAR2(256);                --출근시각
      v_quit_time VARCHAR2(256);                   --퇴근시각
      v_leave_time VARCHAR2(256);                   --외출시각
      v_return_time VARCHAR2(256);                   --귀사시각
      v_early_leave_time VARCHAR2(256);            --조퇴시각

      v_work_hour VARCHAR2(256);                   --근무시간
      v_leave_hour VARCHAR2(256);                   --외출시간
      v_late_hour VARCHAR2(256);                   --지각시간
      v_over_work_hour VARCHAR2(256);             --연장시간
      v_night_work_hour VARCHAR2(256);             --심야시간

      v_attend_type VARCHAR2(256);                --근태 시간계산(출근,지각)
      v_quit_type VARCHAR2(256);                   --근태 시간계산 (연장,심야,퇴근,조퇴)
    v_apply_year VARCHAR2(256);                      --기준년도
      v_approval_code VARCHAR2(256);             --승인여부체크
        
         v_common_quit_time VARCHAR2(256); --일반퇴근시간
     v_after_mn_quit_time VARCHAR2(256); --심야 자정넘어서 퇴근시간
        
BEGIN
   v_apply_year :='2018';      --현재 기준 년도
   SAVEPOINT SP2;

   -- *************************************************************************
    --  회사 기초 근무시간 가져오기.
    -- *************************************************************************

   BEGIN
       SELECT
              ATTEND_TIME,
            QUIT_TIME,
            OVER_TIME,
            NIGHT_TIME,
            LUNCH_START_TIME,
            LUNCH_END_TIME,
             DINNER_START_TIME,
            DINNER_END_TIME
        INTO
              v_base_attend_time,          --기본출근시각
             v_base_quit_time,             --기본퇴근시각
            v_base_over_time,             --기본연장시각
            v_base_night_time,             --기본심야시각
            v_base_lunch_start_time,       --기본점심시작시각
            v_base_lunch_end_time,          --기본점심종료시각
        v_base_dinner_start_time,               --기본저녁시작시각
            v_base_dinner_end_time           --기본저녁종료시각
       FROM
           BASE_WORK_TIME
      WHERE
           APPLY_YEAR=v_apply_year;

      EXCEPTION
           WHEN NO_DATA_FOUND THEN
                  p_error_code:='-1';
                p_error_msg:='등록된 회사기초시간이 없습니다.';
                ROLLBACK TO SP2;
                  RETURN;
  END;

  --점심시간계산
  v_base_lunch_hour:=F_CALC_TIME(v_base_lunch_start_time,v_base_lunch_end_time);

    --저녁시간계산
  v_base_dinner_hour:=F_CALC_TIME(v_base_dinner_start_time,v_base_dinner_end_time);

  FOR FOR_DAY_ATTD_MGT IN(
       SELECT *
      FROM
           DAY_ATTD_MGT
      WHERE
           BASIC_DAY=p_base_day
  )LOOP

  --변수초기화
        v_attend_time:=0;
        v_quit_time:=0;
        v_leave_time:=0;
        v_return_time:=0;
        v_early_leave_time:=0;

        v_work_hour:=0;
        v_late_hour:=0;
        v_over_work_hour:=0;
        v_night_work_hour:=0;
        v_leave_hour:=0;


          v_attend_type:=NULL;
        v_quit_type:=NULL;
        v_approval_code:=NULL;

        --출근타입 SETTING
        v_attend_type:=FOR_DAY_ATTD_MGT.ATTEND_TYPE_CODE;
         
        --퇴근타입 SETTING
        v_quit_type:=FOR_DAY_ATTD_MGT.QUIT_TYPE_CODE;

        --출근시각
        v_attend_time:=FOR_DAY_ATTD_MGT.ATTEND_TIME;

        --퇴근시각
        v_quit_time:=FOR_DAY_ATTD_MGT.QUIT_TIME;

        --외출시각
        v_leave_time:=FOR_DAY_ATTD_MGT.LEAVE_TIME;

        --귀사시각
        v_return_time:=FOR_DAY_ATTD_MGT.RETURN_TIME;

        --조퇴시각
        v_early_leave_time:=FOR_DAY_ATTD_MGT.EARLY_LEAVE_TIME;

      -- 외출시간 계산 : 외출시각이 있다면 귀사시각 - 외출시각
        IF v_leave_time IS NOT NULL THEN
         IF v_return_time >= v_base_lunch_end_time
               AND v_leave_time <= v_base_lunch_start_time THEN
            v_leave_hour := F_CALC_TIME(v_leave_time,v_return_time)-1;
         ELSIF v_leave_time >= v_base_lunch_end_time THEN
               v_leave_hour := F_CALC_TIME(v_leave_time,v_return_time);
         ELSIF v_return_time <= v_base_lunch_start_time THEN
               v_leave_hour := F_CALC_TIME(v_leave_time,v_return_time);
         END IF;
      ELSE
            v_leave_hour := 0;
      END IF;

      -- 지각시간 계산 :: ( 실제출근시각 > 출근기준시각)일 경우      실제출근시각 - 출근기준시각
      IF v_attend_time > v_base_attend_time THEN
            v_late_hour := F_CALC_TIME(v_base_attend_time,v_attend_time);
      ELSE
            v_late_hour := 0;
      END IF;

      -- 조퇴시, 근무시간계산
      IF v_quit_type = 'AQC006' THEN

            IF    v_early_leave_time > v_base_lunch_start_time THEN
         -- 조퇴시각 > 점심시작시각
          -- 근무시간 =(조퇴시각 - 출근시각)-점심시간
               v_work_hour := F_CALC_TIME(v_attend_time,v_early_leave_time)-v_base_lunch_hour;


            ELSE
         -- 조퇴시각 < 점심시작시각
        -- 근무시간 = (조퇴시각 - 출근시각)
               v_work_hour := F_CALC_TIME(v_attend_time,v_quit_time);
            END IF;
      ELSE --조퇴가 아니면
            -- 연장,심야근무일때(승인허가)
            IF (v_quit_type = 'AQC001' OR v_quit_type ='AQC002') THEN
                   -- 심야근무일때(당일퇴근)
                IF v_quit_time > v_base_night_time THEN
                    -- 연장시간 = 기본심야시각 - 기본연장시각
                    -- 심야시간 = 퇴근시각 - 기본심야시각
                    -- 근무시간 = 퇴근시각 - 출근시각 - 점심,저녁시간 (2시간)
                    v_over_work_hour := F_CALC_TIME(v_base_over_time,v_base_night_time);
                    v_night_work_hour := F_CALC_TIME(v_base_night_time,v_quit_time);
                    v_work_hour :=  F_CALC_TIME(v_attend_time,v_base_quit_time)-v_base_lunch_hour;

                     -- 연장근무일때
                ELSIF v_base_night_time > v_quit_time AND v_quit_time > v_base_over_time THEN
                      --연장시간 = 퇴근시각 - 기본연장시간
                    --심야시간 = 0
                    --근무시간 = 퇴근시각 - 출근시각 -1(점심시간)
                    v_over_work_hour := F_CALC_TIME(v_base_over_time,v_quit_time);
                    v_night_work_hour := 0;
                    v_work_hour := F_CALC_TIME(v_attend_time,v_base_quit_time)-v_base_lunch_hour;

                -- 심야근무일때(익일퇴근)-자정을 넘어서 퇴근햇을경우 (출근시각 > 퇴근시각)
                ELSIF 0<=v_quit_time AND v_quit_time<0900 THEN   
                    -- 연장시간 = 기본심야시각 - 기본연장시간
                    -- 심야시간 = 퇴근시각 - 기본심야시간 + 24
                    -- 근무시간 = 퇴근시각 - 출근시각 -1(점심시간)
                    v_over_work_hour := F_CALC_TIME(v_base_over_time,v_base_night_time);
                    v_night_work_hour := ROUND((to_date(v_quit_time, 'HH24:MI') - to_date(v_base_night_time, 'HH24:MI'))*24,1)+24;
                    v_work_hour := F_CALC_TIME(v_attend_time,v_base_quit_time)-v_base_lunch_hour;
                      
                
                       
                       
                --기본연장시각 > 퇴근시각
                ELSE
                      --연장시간 = 0
                  --심야시간 = 0
                  --근무시간 = 퇴근시각 - 출근시각 - 점심시간 (1)
                    v_over_work_hour := 0;
                    v_night_work_hour := 0;
                    v_work_hour :=  F_CALC_TIME(v_attend_time,v_quit_time)-v_base_lunch_hour;
                END IF;
            ELSE
                
                  BEGIN
                 SELECT TIME
                 INTO v_common_quit_time    --일반퇴근시간
                 FROM
                   DAY_ATTD
                 WHERE
                     BASIC_DAY=p_base_day
                 AND ATTD_TYPE_CODE='ADC002'
                 AND TIME>0900;
                 EXCEPTION
               WHEN NO_DATA_FOUND THEN
                   v_common_quit_time := 'N';  --일반퇴근이 안찍혔을때 N 이라고 값을 준다
                END;
      
          BEGIN
                 SELECT TIME
                  INTO v_after_mn_quit_time --자정이후퇴근시간
                  FROM
                   DAY_ATTD
                  WHERE
                      BASIC_DAY=p_base_day+1 --다음날 기준으로
                  AND ATTD_TYPE_CODE='ADC002'
                  AND TIME<0900;  --퇴근시간이 9시 안으로 일경우
                   EXCEPTION
               WHEN NO_DATA_FOUND THEN
                   v_after_mn_quit_time := 'N'; --자정 넘어 심야퇴근시간이 없으면 N 이라고 설정
                END; 
                
                
                --출근은 했는데 퇴근을 안한경우 결근처리한다.
                IF v_common_quit_time='N' AND v_after_mn_quit_time='N' AND v_attend_time IS NOT NULL THEN
                      --근무시간 = 0
                    v_work_hour:=0;
                   BEGIN
                       UPDATE
                        DAY_ATTD_MGT
                     SET
                        ATTEND_TYPE_CODE ='AQC007'
                     WHERE
                        BASIC_DAY = p_base_day
                     AND
                        EMP_CODE = FOR_DAY_ATTD_MGT.EMP_CODE;
                 END;
              ELSE
              --정상근무 경우
              --근무시간 = 기본퇴근시간 - 출근시간 - 점심시간
               v_work_hour:=F_CALC_TIME(v_attend_time,v_quit_time)-1 ;
                  END IF;
          END IF;
      END IF;

      --결근 근무시간계산(근무시간 = 0)
    IF v_attend_type = 'AQC007' THEN
           v_work_hour:=0;
    END IF;

          --연차 근무시간계산(근무시간 = 8)
    IF v_attend_type = 'AQC013' THEN
           v_work_hour:=8;
    END IF;

    --외출 시간이 있다면 근무시간 차감
    IF v_leave_hour > 0 THEN
          v_work_hour:=v_work_hour - v_leave_hour;
    END IF;

    BEGIN
       UPDATE
            DAY_ATTD_MGT
      SET
           ATTEND_TIME = v_attend_time,
          QUIT_TIME = v_quit_time,
          LEAVE_TIME = v_leave_time,
          RETURN_TIME = v_return_time,
          EARLY_LEAVE_TIME = v_early_leave_time,
          WORK_HOUR = v_work_hour,
          LATE_HOUR = v_late_hour,
          OVER_WORK_HOUR = v_over_work_hour,
          NIGHT_WORK_HOUR = v_night_work_hour,
          LEAVE_HOUR = v_leave_hour
      WHERE
            BASIC_DAY = p_base_day
      AND
            EMP_CODE = FOR_DAY_ATTD_MGT.EMP_CODE;
    END;
  END LOOP;
  COMMIT;
    p_error_code:='0';
    p_error_msg:='P__DAILY_ATTD_CALC Success ';
END P_DAILY_ATTD_CALC;


