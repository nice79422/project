  CREATE OR REPLACE PACKAGE BODY "TEST4"."P_HR_ANNUAL" AS
PROCEDURE P_CREATE_ANNUAL(
		p_emp_code IN VARCHAR2,    			--사원코드
		p_standard_year IN VARCHAR2,   		--기준년도
		p_hire_date IN VARCHAR2,   			--입사일자
		p_error_code OUT VARCHAR2, 			--에러코드
		p_error_msg OUT VARCHAR2   			--에러메시지
)
IS
   		v_emp_code                    VARCHAR2(100);            --사번
 		v_standard_year   	          VARCHAR2(100);            --기준년도
   		v_base_hire_date              VARCHAR2(100);            --기준년도 + 입사한 월/일
 		v_hire_date       	          VARCHAR2(100);            --입사날짜
 		v_length_of_service           VARCHAR2(100);            --근속년도
 		   v_usable_days            	    VARCHAR2(100);            --사용가능한 연차일수
    v_max_annual                  VARCHAR2(100);            --최고연차
		v_base_annual                 VARCHAR2(100);            --기본연차
		v_used_days                   VARCHAR2(100);            --해당 년도에 사용한 연차 합계
		v_rest_days                   VARCHAR2(100);            --잔여연차 일 수
    v_emp_annual                  VARCHAR2(100);            --1년이상부터, 근속년도 +1일

BEGIN
		v_emp_code := p_emp_code;
		v_standard_year := p_standard_year ;
		v_max_annual:='25';                                      --최고연차:25일.
		v_base_annual:='15';                                     --기본연차:15일.
		v_hire_date:=p_hire_date;
		v_used_days:='0';
    SAVEPOINT SP3;

    BEGIN
        SELECT v_standard_year || SUBSTR(p_hire_date,5,4)
        INTO v_base_hire_date
        FROM DUAL;
    END;

  	BEGIN
      	SELECT
          TO_CHAR(TO_DATE(v_base_hire_date ,'YYYYMMDD'),'YYYY')-TO_CHAR(TO_DATE(v_hire_date ,'YYYYMMDD'),'YYYY')
        INTO v_length_of_service
        FROM DUAL;

        --근속년수가 없을경우 연차생성안됨(입사한지 1년안된경우)
        IF v_length_of_service = 0  THEN
        	--사용가능한연차일수, 잔여연차일수를 0으로 SETTING
         	v_usable_days := '0';
         	v_rest_days := '0';

        --근속년수가 1년이내일때는 기본 연차일수(15일)
        ELSIF	v_length_of_service <= 1  THEN
				v_usable_days := v_base_annual;
				v_rest_days := v_base_annual;

        --1년이상 사원은 기본 15일 그이후는 1년마다 1일씩 증가
        ELSIF	v_length_of_service > 1 THEN
        		BEGIN
            		SELECT v_length_of_service-1
                INTO v_emp_annual
                FROM DUAL;

                v_usable_days:=v_base_annual+v_emp_annual;
                v_rest_days:=v_base_annual+v_emp_annual;

                IF v_usable_days>=25     -- 최대 연차 25일 이상은 무조건 25일로 변경.
                THEN
               		v_usable_days:=v_max_annual;
            			v_rest_days:=v_max_annual;
                END IF;

            EXCEPTION WHEN OTHERS THEN
           		p_error_code:=-1;
                p_error_msg:='1년이상 근속 계산중 오류가 발생하였습니다. ';
                ROLLBACK TO SP3;
                RETURN;
        		END;
        END IF;

    EXCEPTION WHEN OTHERS THEN
      p_error_code:=-1;
      p_error_msg:='사원의 근속년수/연차사용 일 수 계산 중 오류가 발생하였습니다.';
      ROLLBACK TO SP3;
      RETURN;
    END;

	--연차관리테이블에 INSERT
	BEGIN
      INSERT INTO ANNUAL_MGT(EMP_CODE, STANDARD_YEAR, REST_DAYS, USABLE_DAYS, USED_DAYS, LENGTH_OF_SERVICE) VALUES( v_emp_code, v_standard_year, v_rest_days, v_usable_days, v_used_days, v_length_of_service);
      COMMIT;

      EXCEPTION WHEN OTHERS THEN
      p_error_code:=-1;
      p_error_msg:='연차관리테이블에 데이터 INSERT중 오류가 발생하였습니다.';
      ROLLBACK TO SP3;
      RETURN;
	END;

  p_error_code:=0;
  p_error_msg:='연차관리테이블에 데이터 INSERT를 성공하였습니다.';

END P_CREATE_ANNUAL;
END P_HR_ANNUAL;

