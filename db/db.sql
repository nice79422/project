--월근태 계산 프로시저

PROCEDURE P_CREATE_MONTH_ATTD(
   p_base_year_month IN VARCHAR2,
    p_error_code OUT VARCHAR2,
    p_error_msg OUT VARCHAR2
)
IS
      v_base_attend_time VARCHAR2(256);                --기본출근시각
    v_base_quit_time VARCHAR2(256);                --기본퇴근시각
     v_base_lunch_start_time VARCHAR2(256);          --기본점심시작시각
    v_base_lunch_end_time VARCHAR2(256);          --기본점심종료시각
     v_base_dinner_start_time VARCHAR2(256);          --기본점심시작시각
    v_base_dinner_end_time VARCHAR2(256);          --기본점심종료시각

    v_basic_work_hour VARCHAR2(256);                --기본근무시간
     v_work_hour VARCHAR2(256);                      --실제근무시간
    v_over_work_hour VARCHAR2(256);                --연장근무시간
    v_night_work_hour VARCHAR2(256);                --심야근무시간
    v_late_hour VARCHAR2(256);                      --지각시간

    v_basic_work_days VARCHAR2(256);                --기본근무일수
    v_weekday_work VARCHAR2(256);                --평일근무일수
    v_absent_days VARCHAR2(256);                   --결근일수
    v_late_days VARCHAR2(256);                      --지각일수
    v_leave_early_days VARCHAR2(256);                --조퇴일수
    v_leave_days VARCHAR2(256);                      --외출일수
    v_attd_rest_days VARCHAR2(256);                   --근태외일수(출장,교육)

    v_holiday_work_days VARCHAR2(256);             --휴일근무일수
    v_holiday_work_hour VARCHAR2(256);             --휴일근무시간
    v_holiday_over_work_hour VARCHAR2(256);       --휴일연장근무시간
    v_holiday_night_work_hour VARCHAR2(256);      --휴일심야근무시간
    v_apply_year VARCHAR2(256);                      --기준년도
    v_close_yn VARCHAR2(256);                         --마감여부

BEGIN
   v_apply_year :='2018';      --현재 기준 년도
   SAVEPOINT SP3;

    -- ************************************************************************
    -- 마감된 월은 월근태관리(MONTH_ATTD_MGT)에서 삭제안하고 프로시저 종료.
    -- 마감되지 않은 월은 월근태관리(MONTH_ATTD_MGT)에서 삭제후 새로운 데이터 삽입.
    -- 마감구분 ( 마감 = 'Y' , 마감안함 = 'N')
    -- 새로운 데이터 프로시저 돌리려면 마감취소후 다시 돌려야함
    -- ************************************************************************

   BEGIN
       SELECT
           DISTINCT(CLOSE_YN)
      INTO v_close_yn
      FROM MONTH_ATTD_MGT
      WHERE
           BASIC_YEAR_MONTH = p_base_year_month;
      EXCEPTION
            WHEN OTHERS THEN
                p_error_code:='0';
              p_error_msg:='Continue Procedure';
  END;

   -- 기준년월의 데이터가 마감되었으면 프로시저 종료
  IF v_close_yn = 'Y' THEN
     p_error_code:='0';
     p_error_msg:='P_CREATE_MONTH_ATTD success';
     RETURN;

  ELSE

    -- *****************************************************************************
    -- 일근태현황(DAY_ATTD_MGT) 데이터를 종합해서 월근태현황(MONTH_ATTD_MGT) 데이터를 업데이트
    -- *****************************************************************************
    -- 기준월의 마감되지않은 데이터를 모두 지운다.

    BEGIN
       DELETE
           MONTH_ATTD_MGT
      WHERE
           BASIC_YEAR_MONTH=p_base_year_month;
      EXCEPTION
            WHEN OTHERS THEN
                p_error_code:='-1';
                p_error_msg:='기준년월의 데이터를 삭제하는데 실패하였습니다.';
                ROLLBACK TO SP3;
                RETURN;
    END;

    -- 월근태현황 테이블에 모든 사원+기준년월 데이터를 넣는다
    FOR for_employee IN (
        SELECT * FROM EMPLOYEE
    )LOOP

      BEGIN
           INSERT INTO
               MONTH_ATTD_MGT(
                      EMP_CODE,
                    BASIC_YEAR_MONTH,
                    CLOSE_YN
              )
          VALUES(
                     for_employee.EMP_CODE,
                   p_base_year_month,
                   'N'
            );

      EXCEPTION
            WHEN others THEN
                   p_error_code:='-1';
                  p_error_msg:='기준월 데이터 생성실패';
                  ROLLBACK TO SP3;
                  RETURN;
      END;
    END LOOP;

  -- *************************************************************************
    --  회사 기초시간 가져오기.
    -- *************************************************************************

   BEGIN
       SELECT
           ATTEND_TIME,
            QUIT_TIME,
            LUNCH_START_TIME,
            LUNCH_END_TIME,
            DINNER_START_TIME,
            DINNER_END_TIME
      INTO
             v_base_attend_time,          --기본출근시각
           v_base_quit_time,             --기본퇴근시각
            v_base_lunch_start_time,    --기본점심시작시각
            v_base_lunch_end_time,    --기본점심종료시각
            v_base_dinner_start_time,    --기본저녁시작시각
            v_base_dinner_end_time    --기본저녁종료시각
       FROM
           BASE_WORK_TIME
      WHERE
           APPLY_YEAR= v_apply_year;
      EXCEPTION
           WHEN NO_DATA_FOUND THEN
                  p_error_code:='-1';
                p_error_msg:='해당년도에 등록된 회사기초시간이 없습니다!!';
                ROLLBACK TO SP3;
                  RETURN;
  END;

    -- ****************************************************************************
    -- 기본근무일수, 기본근무시간 구하기
    -- 기본근무일수 = 해당년도 월의 일수 - 해당년도 월의 휴일일수(HOLIDAY_MGT)
    -- 기본근무시간 = 기본근무일수 * (기본퇴근시각 - 기본출근시각 - 점심시간(점심종료시간-점심시작시간))
    -- ****************************************************************************

  BEGIN
       --기본근무일수 구하기   -토,일,공휴일 제외
        SELECT COUNT(1) CNT INTO v_basic_work_days
           FROM (SELECT TO_CHAR(SDT + LEVEL - 1, 'YYYYMMDD') DT
                 , TO_CHAR(SDT + LEVEL - 1, 'D') D
                  FROM (SELECT TO_DATE(p_base_year_month||'01', 'YYYYMMDD') SDT
                      , LAST_DAY(TO_DATE(p_base_year_month||'01','YYYYMMDD')) EDT
                        FROM DUAL)
         CONNECT BY LEVEL <= EDT - SDT + 1   --for문 처럼 반복함 구글링ㄱㄱ.
         ) A
      , ( SELECT basic_Day FROM HOLIDAY_MGT WHERE basic_Day LIKE p_base_year_month||'%' ) B
      WHERE A.DT = B.basic_Day(+)
      AND A.D NOT IN ('1', '7') --토,일 제외
      AND B.basic_Day IS NULL;
 
 

      --기본근무시간 구하기
      --예를 들어 시간이 6시이면 0600으로 저장되기때문에 100으로 나눠준다.
      v_basic_work_hour:=v_basic_work_days*(v_base_quit_time-v_base_attend_time-(v_base_lunch_end_time-v_base_lunch_start_time))/100;
  END;

    -- ****************************************************************************
    -- 월근태 계산 (평일)
    -- ****************************************************************************
    FOR for_month_attd IN (
       SELECT * FROM
           MONTH_ATTD_MGT
      WHERE
           BASIC_YEAR_MONTH = p_base_year_month
    ) LOOP
          --근무시간 시간총합 계산(AQC010:정상근무일)
         --기준년월의 일근태 테이블에서 정상근무일이고 마감되었을때 각 사원의 근무시간을 계산한다.
        BEGIN
           SELECT
               SUM(WORK_HOUR),
                SUM(OVER_WORK_HOUR),
                SUM(NIGHT_WORK_HOUR),
                SUM(LATE_HOUR)
          INTO
              v_work_hour,v_over_work_hour,v_night_work_hour,v_late_hour
        FROM
              DAY_ATTD_MGT
        WHERE
              EMP_CODE = for_month_attd.EMP_CODE
        AND
              BASIC_DAY LIKE (p_base_year_month || '%')
        AND
              CLOSE_YN = 'Y'
        AND
              ATTD_DAY_TYPE = 'AQC010';
        EXCEPTION
               WHEN no_data_found THEN
                   p_error_code := '-1';
                  p_error_msg := '기준년월일 '||p_base_year_month ||'에 마감된 일자가 없습니다.';
                  ROLLBACK TO SP3;
                  RETURN;
              WHEN others THEN
                   p_error_code := '-1';
                  p_error_msg := '기준년월 근태시간 계산에 실패하였습니다.';
                  ROLLBACK TO SP3;
                  RETURN;

        END;

        --근태일수 계산
        BEGIN
           SELECT
                  COUNT(CASE WHEN ATTEND_TIME IS NOT NULL THEN 1 ELSE NULL END),             --출근일수
                COUNT(CASE WHEN ATTEND_TYPE_CODE = 'AQC007' THEN 1 ELSE NULL END),       --결근일수
                COUNT(CASE WHEN ATTEND_TYPE_CODE = 'AQC003' THEN 1 ELSE NULL END),       --지각일수
                COUNT(CASE WHEN QUIT_TYPE_CODE = 'AQC006' THEN 1 ELSE NULL END),             --조퇴일수
                COUNT(CASE WHEN LEAVE_HOUR != 0 THEN 1 ELSE NULL END)                         --외출일수
          INTO
                  v_weekday_work, v_absent_days, v_late_days, v_leave_early_days, v_leave_days
          FROM
                  DAY_ATTD_MGT
          WHERE
                  EMP_CODE = for_month_attd.EMP_CODE
          AND
                  BASIC_DAY LIKE (p_base_year_month || '%')
          AND
                  CLOSE_YN = 'Y'
          AND
                  ATTD_DAY_TYPE = 'AQC010';
        END;


        --근태외일수 계산(근태외가 휴일에 포함되엇을경우 함께계산) 연차,출장,교육
            --연차는 출근퇴근 시간이 비는데. 출장교육은 0900 ~ 1800 시간 자동 입력됨.
        BEGIN
           SELECT
                COUNT(CASE WHEN ATTEND_TYPE_CODE = 'AQC011' THEN 1
                                WHEN ATTEND_TYPE_CODE = 'AQC012' THEN 1
                           WHEN ATTEND_TYPE_CODE = 'AQC013' THEN 1
                           ELSE NULL
                      END)
          INTO
                 v_attd_rest_days
          FROM
               DAY_ATTD_MGT
          WHERE
               EMP_CODE = for_month_attd.EMP_CODE
          AND
               BASIC_DAY LIKE (p_base_year_month || '%')
          AND
               CLOSE_YN = 'Y';
        END;

      -- ******************************************************************
        -- 계산된 시간 및 일수 업데이트
        -- ******************************************************************

        BEGIN
            UPDATE
                MONTH_ATTD_MGT
            SET
                  BASIC_WORK_HOUR = v_basic_work_hour,
                WORK_HOUR = v_work_hour,
                OVER_WORK_HOUR = v_over_work_hour,
                NIGHT_WORK_HOUR = v_night_work_hour,
                LATE_HOUR = v_late_hour,
                BASIC_WORK_DAYS = v_basic_work_days,
                WEEKDAY_WORK = v_weekday_work,
                ABSENT_DAYS = v_absent_days,
                LATE_DAYS = v_late_days,
                LEAVE_EARLY_DAYS = v_leave_early_days,
                LEAVE_DAYS = v_leave_days,
                ATTD_REST_DAYS = v_attd_rest_days
            WHERE
               BASIC_YEAR_MONTH = p_base_year_month
            AND
               EMP_CODE = for_month_attd.EMP_CODE;

        END;
    END LOOP;

    -- ****************************************************************
    -- 월근태 계산 (휴일)
    -- ****************************************************************

   FOR FOR_MONTH_ATTD_HOLIDAY IN(
       SELECT * FROM
           MONTH_ATTD_MGT
      WHERE
           BASIC_YEAR_MONTH = p_base_year_month
  )LOOP
       --휴일근무시간 시간총합계산(휴일) AQC009 휴일
      BEGIN
           SELECT
                  SUM(WORK_HOUR),
                SUM(OVER_WORK_HOUR),
                SUM(NIGHT_WORK_HOUR)
          INTO
               v_holiday_work_hour,v_holiday_over_work_hour,v_holiday_night_work_hour
          FROM
               DAY_ATTD_MGT
          WHERE
               EMP_CODE = FOR_MONTH_ATTD_HOLIDAY.EMP_CODE
          AND
               BASIC_DAY LIKE (p_base_year_month || '%')
          AND
               CLOSE_YN = 'Y'
          AND
               ATTD_DAY_TYPE = 'AQC009';
      END;

      --근태일수 계산(휴일)
      BEGIN
           SELECT
                --휴일 출근일수
               COUNT(CASE WHEN ATTEND_TIME IS NOT NULL THEN 1 ELSE NULL END)
          INTO
               v_holiday_work_days
          FROM
               DAY_ATTD_MGT
          WHERE
               EMP_CODE = FOR_MONTH_ATTD_HOLIDAY.EMP_CODE
          AND
               BASIC_DAY LIKE (p_base_year_month || '%')
          AND
               CLOSE_YN = 'Y'
          AND
               ATTD_DAY_TYPE = 'AQC009';
      END;

        -- **************************************************************
        -- 계산된 시간 및 일수 업데이트
        -- **************************************************************

      BEGIN
         UPDATE
           MONTH_ATTD_MGT
        SET
          HOLIDAY_WORK_DAYS = v_holiday_work_days,
          HOLIDAY_WORK_HOUR = v_holiday_work_hour,
          HOLIDAY_OVER_WORK_HOUR = v_holiday_over_work_hour,
          HOLIDAY_NIGHT_WORK_HOUR = v_holiday_night_work_hour
        WHERE
           BASIC_YEAR_MONTH = p_base_year_month
        AND
           EMP_CODE = FOR_MONTH_ATTD_HOLIDAY.EMP_CODE;
      END;

  END LOOP;

END IF;
   p_error_code:='0';
   p_error_msg:=v_basic_work_hour;
END P_CREATE_MONTH_ATTD;








END P_HR_ATTENDANCE;

